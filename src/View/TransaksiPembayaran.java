    private void showComboBoxCucian() throws SQLException {
        DefaultComboBoxModel dcbmpelanggan = new DefaultComboBoxModel();

        for (Transaksi_cucian dp : this.transaksi.getDataCucian()) {
            dcbmpelanggan.addElement(dp.getId_Transaksi());
        }
        this.cbIdtransaksi.setModel(dcbmpelanggan);
    }
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
            Transaksi_pembayaran tp = new Transaksi_pembayaran();
                try { 
                        tp.setTransaksi_cucian(this.transaksi.getDataCucian().get(this.cbIdtransaksi.getSelectedIndex()));
                        tp.setHarga_Total(Integer.parseInt(HargaTotal.getText()));
                        tp.setTunai(Integer.parseInt(Tunai.getText()));
                        tp.setKembalian(Integer.parseInt(Kembalian.getText()));
                         if(jRadioButton1.isSelected()){
                             tp.setStatus(jRadioButton1.getText());
                         }else if(jRadioButton2.isSelected()){
                             tp.setStatus(jRadioButton2.getText());
                         }else{
                            JOptionPane.showMessageDialog(null, "Tentukan Pilihan ");
                         }
                        this.showComboBoxCucian(); 
                        this.transaksi.getDataPembayaran();
                        this.transaksi.insertPembayaran(tp);
                        this.showTabelPembayaran();
                        kosong();
                } catch (SQLException ex) {
                    Logger.getLogger(Transaksi_cucian.class.getName()).log(Level.SEVERE, null, ex);
                }
    }//GEN-LAST:event_jButton2ActionPerformed

