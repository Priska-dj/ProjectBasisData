    public void showTabelPembayaran() throws SQLException {
        DefaultTableModel dtmBayar = new DefaultTableModel(new String[]
        {"ID Pembayaran", "Berat Cucian", "Harga Total", "Tunai", "Kembalian", "Status"}, 0);
        dtmBayar.setRowCount(0);

        for (Transaksi_pembayaran tp : this.transaksi.getDataPembayaran()) {
            dtmBayar.addRow(new String[]{tp.getId_Pembayaran().toString(), tp.getTransaksi_cucian().getBerat().toString(),tp.getHarga_Total().toString(),
                tp.getTunai().toString(), tp.getKembalian().toString(), tp.getStatus()});
        }

        this.tblKeluar.setModel(dtmBayar);
    }
    
    public void showTabelCucian() throws SQLException{
        DefaultTableModel model = new DefaultTableModel();
        model.setRowCount(0);
        model.addColumn("ID TRANSAKSI");
        model.addColumn("NAMA PELANGGAN");
        model.addColumn("JENIS CUCIAN");
        model.addColumn("HARGA JENIS CUCIAN");
        model.addColumn("BERAT CUCIAN");
        
        String kodeSql = "SELECT * FROM Data_cucian ORDER BY Id_transaksi ASC";
        ResultSet res = this.koneksi.GetData(kodeSql);
        while (res.next()) {
                model.addRow(new Object[]{res.getString(1), res.getString(2), res.getString(3), res.getString(4), res.getString(5)});
            }
        datacucian.setModel(model);
    }
