    private void showComboBoxPelanggan() throws SQLException {
        DefaultComboBoxModel dcbmpelanggan = new DefaultComboBoxModel();

        for (Data_Pelanggan dp : this.cuciancon.getDataPelanggan()) {
            dcbmpelanggan.addElement(dp.getNama_Pelanggan());
        }
        this.cbpelanggan.setModel(dcbmpelanggan);
    }

    public void showTabelCucian() throws SQLException {
        DefaultTableModel dtmCucian = new DefaultTableModel(new String[]
        {"ID Transaksi", "Nama Pelanggan", "Berat Cucian", "Tanggal Masuk", "Tanggal Pengambilan"}, 0);
        dtmCucian.setRowCount(0);

        for (Transaksi_cucian tc : this.cuciancon.getDataCucian()) {
            dtmCucian.addRow(new String[]{tc.getDataPelanggan().getId_Pelanggan().toString(), tc.getDataPelanggan().getNama_Pelanggan(),
                tc.getBerat().toString(),tc.getTanggal_Masuk().toString(),tc.getTanggal_Pengambilan().toString()});
        }

        this.tbltransaksi.setModel(dtmCucian);
    }
