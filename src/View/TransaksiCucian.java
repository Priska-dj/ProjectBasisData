    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        Transaksi_cucian tc = new Transaksi_cucian();
        try {
            tc.getId_Transaksi();
            tc.setDataPelanggan(this.cuciancon.getDataPelanggan().get(this.cbpelanggan.getSelectedIndex()));
            tc.setBerat(Integer.parseInt(berat.getText()));
            tc.setTanggal_Masuk(tanggalMasuk.getDate());
            tc.setTanggal_Pengambilan(tglKeluar.getDate());
            
            this.showComboBoxPelanggan();
            this.cuciancon.getDataCucian();
            this.cuciancon.insertCucian(tc);
            this.showTabelCucian();
            berat.setText("");
            tanggalMasuk.setDateFormatString("");
            tglKeluar.setDateFormatString("");
        } catch (SQLException ex) {
            Logger.getLogger(Transaksi_cucian.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

