    private void showComboBoxJenisCucian() throws SQLException {
        DefaultComboBoxModel dcbmJenis = new DefaultComboBoxModel();

        for (Jenis_cucian jc : this.pelanggancon.getDataJenis()) {
            dcbmJenis.addElement(jc.getNama_jenis_cucian());
        }
        this.CbJenisCucian.setModel(dcbmJenis);
    }

    public void showTabelPelanggan() throws SQLException {
        DefaultTableModel dtmPelanggan = new DefaultTableModel(new String[]
        {"ID Pelanggan", "Jenis Cucian", "Nama Pelanggan", "No Telp", "Alamat"}, 0);
        dtmPelanggan.setRowCount(0);

        for (Data_Pelanggan dp : this.pelanggancon.getDataPelanggan()) {
            dtmPelanggan.addRow(new String[]{dp.getId_Pelanggan().toString(), dp.getJenisCucian().getNama_jenis_cucian(),
                dp.getNama_Pelanggan(), dp.getNo_Tlp(), dp.getAlamat()});
        }

        this.TblMasuk.setModel(dtmPelanggan);
    }

    private void kosong() {

        NamaPelanggan.setText("");
        NoTlp.setText("");
        alamat.setText("");
    }

