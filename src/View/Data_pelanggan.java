    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            int konfirmasi = JOptionPane.showConfirmDialog(null, "Yakin Ingin Hapus Semua Data?",
                    "Konfirmasi", OK_CANCEL_OPTION, QUESTION_MESSAGE);
            if (konfirmasi == 0) {
                this.pelanggancon.clear();

                this.showTabelPelanggan();
                JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Data Gagal Dihapus", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Data_pelanggan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int col = 0;
        int row = TblMasuk.getSelectedRow();
        String id = TblMasuk.getModel().getValueAt(row, col).toString();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Produk belum dipilih, Pilih produk terlebih dahulu !");
        } else {
            System.out.println("ID = " + id);
            Data_Pelanggan dp = new Data_Pelanggan();
        try {
            dp.setId_Pelanggan(Integer.parseInt(id));
            dp.setJenisCucian(this.pelanggancon.getDataJenis().get(this.CbJenisCucian.getSelectedIndex()));
            dp.setNama_Pelanggan(this.NamaPelanggan.getText());
            dp.setNo_Tlp(this.NoTlp.getText());
            dp.setAlamat(this.alamat.getText());
            
            this.showComboBoxJenisCucian();
            this.pelanggancon.getDataPelanggan();
            this.pelanggancon.update(dp);
            this.showTabelPelanggan();
            this.kosong();

        } catch (SQLException ex) {
            Logger.getLogger(Data_pelanggan.class.getName()).log(Level.SEVERE, null, ex);
             }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

